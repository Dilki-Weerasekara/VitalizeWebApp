                                    <ul class="ul_fontclass">
                                        <li>Seeing things in black-and-white terms, with no middle ground.</li>
                                        <li>Overgeneralization: Drawing broad conclusions based on limited evidence or a single negative event.</li>
                                        <li>Catastrophizing: Assuming the worst possible outcome will happen.</li>
                                        <li>Mind reading: Believing you know what others are thinking or assuming they view you negatively.</li>
                                        <li>Discounting the positive: Minimizing or dismissing positive experiences or achievements.</li>
